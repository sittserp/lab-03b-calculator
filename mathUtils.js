export function add(thingOne, thingTwo) {
    return thingOne + thingTwo;
}

export function multiply(thingOne, thingTwo) {
    return thingOne * thingTwo;
}

export function subtract(thingOne, thingTwo) {
    return thingOne - thingTwo;
}

export function divide(thingOne, thingTwo) {
    return thingOne / thingTwo;
}


